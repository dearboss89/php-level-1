        "у" => "u",
        "ф" => "f",
        "х" => "h",
        "ц" => "cz",
        "ч" => "ch",
        "ш" => "sh",
        "щ" => "shh",
        "ъ" => "``",
        "ы" => "y",
        "ь" => "`",
        "э" => "e`",
        "ю" => "yu",
        "я" => "ya"
    ];
    $mas = preg_split('//u', $str, null, PREG_SPLIT_NO_EMPTY);

    for ($i = 0; $i < count($mas); $i++) {
        foreach ($letters as $key => $value) {
            if ($mas[$i] == $key)
                $mas[$i] = $value;
        }
    }
    $str = implode("", $mas);

    return $str;

}

